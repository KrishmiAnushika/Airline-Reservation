    private void loadAllAirports() {
        DefaultTableModel dtm = (DefaultTableModel) tblAirport.getModel();
        dtm.setRowCount(0);
        
        try {
            List<AirportDTO> airports = ManageAirportController.getAllAirports();
            
            if (airports == null) {
                return;
            }
            
            for (AirportDTO airport : airports) {
                Object[]rowData = {
                        airport.getAirportID(),
                        airport.getAirportName(),
                        airport.getCountry()
                        
                    };
                dtm.addRow(rowData);
            }
        } catch (Exception e) {
            Logger.getLogger(AddNewAirline.class.getName()).log(Level.SEVERE,null,e);
        }
    }

