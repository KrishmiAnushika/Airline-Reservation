    private void jLabel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel6MouseClicked
        try {
            ManageAirportController manageAirportController = new ManageAirportController();

            AirportDTO airportDTO = new AirportDTO();
            airportDTO.setAirportID(txtAirportID.getText());
            airportDTO.setAirportName(txtAirportName.getText());
            airportDTO.setCountry(txtCountry.getText());

            boolean isUpdate = ManageAirportController.updateAirport(airportDTO);
            if (isUpdate) {
                JOptionPane.showMessageDialog(this, "Update Success");
                loadAllAirports();
            } else {
                JOptionPane.showMessageDialog(this, "Update fail");
            }
        } catch (Exception ex) {
            Logger.getLogger(AddNewAirline.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jLabel6MouseClicked

